public class Ej1 {

    public static void main(String[] args) {
       
      /*Declara dos variables numerica (con el valor que quieras)
        muestra por consola la suma , resta,multiplicacion , division y modulo
      */  
    
      int numeroUno = 10;
      int numeroDos = 5;
      
      System.out.println("---Operaciones---");
      System.out.println("Suma" + " " + (numeroUno + numeroDos));
      System.out.println("Resta" + " " + (numeroUno - numeroDos));
      System.out.println("Multiplicacion" + " " + (numeroUno*numeroDos));
      System.out.println("Division" + " " + (numeroUno/numeroDos));
      System.out.println("Modulo" + " " + (numeroUno%numeroDos));
    }
}
