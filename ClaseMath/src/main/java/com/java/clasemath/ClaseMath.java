
package com.java.clasemath;

public class ClaseMath {

    public static void main(String[] args) {
       
        /*Clase Math : Ejemplo de los metodos existentes.*/
        
        System.out.println("El valor absoluto de dos numeros : " + Math.abs(-10)); //devuelve 10 positivo.
        
        System.out.println("El valor maximo de estos numeros : " + Math.max(20,50)); //Devuelve el valor maximo.
        
        System.out.println("El valor mininmo de estos numeros es  : " + Math.min(20.50, 30.50)); //Devuelve el valor minimo.
        
        System.out.println("El valor de PI es : " + Math.PI);
        
        System.out.println("El valor de E es : " + Math.E);
        
        System.out.println("Devuelve el numero mas cercano por arriba " + Math.ceil(4.5)); //Redondea.
        
        System.out.println("Devuelve el numero mas cercano por abajo " + Math.floor(4.2)); //Redondea para abajo
    }
}
